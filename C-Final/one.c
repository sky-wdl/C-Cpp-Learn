/*
 * 【问题描述】
假设邮局规定寄邮件时若每件重量在1公斤以内(含1公斤)，按2.5元计算邮费，如果超过1公斤时，
其超出部分每公斤收1.8元。请编程序计算邮件收费。
【输入形式】
公斤数。
【输出形式】
邮件收费，保留2位小数
【样例输入】
5
【样例输出】
9.70
 */
#include<stdio.h>
int main()
{
   double a,s=0;
   scanf("%lf", &a);

   if(a<=1 && a > 0)
   {
 s=2.5;
   }
   else if(a>1){
 s= 2.5 + (a-1)*1.8;
   }
   printf("%.2f",s);
    return 0;
}
