/*
 * 3. 阶梯电价V1
【问题描述】

为了提倡居民节约用电，某省电力公司执行“阶梯电价”，安装一户一表的居民用户电价分为两个“阶梯”：月用电量60千瓦时（含60千瓦时）以内的，电价为0.55元/千瓦时；超过60千瓦时的，超出部分的用电量，电价上调0.1元/千瓦时。请编写程序计算电费。保留2位小数。

【输入形式】

100
【输出形式】

59.00
 */
#include <stdio.h>
 int main()
 {
    int a=0;
    scanf("%d",&a);
    if (a<=60)
    {
        printf("%.2f",(float)a*0.55);
    }
    else
    {
        printf("%.2f",((float)a-60.0)*0.65+60.0*0.55);
    }

    return 0;
 }